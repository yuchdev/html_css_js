// Print into HTML element
function printInElement(element, text) {
    document.getElementById(element).innerHTML = text;
}

// Print using document.write()
function printUsingDocumentWrite(text) {
    document.write(text);
}

// Message box with window.alert()
function printUsingAlert(text) {
    window.alert(text);
}

// Execution log with console.log()
function printUsingConsoleLog(text) {
    console.log(text);
}


